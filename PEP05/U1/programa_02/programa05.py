#Escribe un programa que pregunte al usuario dos números y calcule su suma, resta, multiplicación, división, módulo y potencia.

var_1=int(input("Introduce el primer número:"))
var_2=int(input("Introduce el segundo número:"))

print(f"La suma entre {var_1} + {var_2} es : {var_1 + var_2}")
print(f"La resta entre {var_1} - {var_2} es : {var_1 - var_2}")
print(f"La multiplicación entre {var_1} * {var_2} es : {var_1 * var_2}")
print(f"La división entre {var_1} / {var_2} es : {var_1 / var_2}")
print(f"El módulo entre {var_1} % {var_2} es : {var_1 % var_2}")
print(f"La potencia entre {var_1} ** {var_2} es : {var_1 ** var_2}")